A = round(float(input('Digite o valor A: ')))
B = round(float(input('Digite o valor B: ')))

print(f'\nO valor de A foi definido como {A}\nO valor de B foi definido como {B}\n')

adicao = A + B
subtracao = A - B
multiplicacao = A * B
divisao = A / B

print(f'A adição entre A e B é igual a: {adicao}')
print(f'A subtração entre A e B é igual a: {subtracao}')
print(f'A multiplicação entre A e B é igual a: {multiplicacao}')
print(f'A divisão entre A e B é igual a: {divisao}')